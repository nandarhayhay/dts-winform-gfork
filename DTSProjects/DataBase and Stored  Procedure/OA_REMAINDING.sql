	AND BBS.LEFT_QTY > 0 AND BBS.RELEASE_QTY > 0 AND BBS.DISTRIBUTOR_ID = @DISTRIBUTOR_ID
	AND YEAR(AA.END_DATE) >= (YEAR(GETDATE()) -1)
    END
