			  )REM
	  ON OOB.OA_BRANDPACK_ID = REM.OA_BRANDPACK_ID
	  WHERE OPO.PO_REF_DATE >= @START_DATE
	  AND OPB.PO_ORIGINAL_QTY > 0  
	  GROUP BY OPO.DISTRIBUTOR_ID,OPB.BRANDPACK_ID
