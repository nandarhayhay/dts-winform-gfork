		      INNER JOIN DISTRIBUTOR_AGREEMENT DA ON DA.AGREEMENT_NO = AA.AGREEMENT_NO
		      WHERE DA.DISTRIBUTOR_ID = BBS.DISTRIBUTOR_ID AND AA.START_DATE >= @START_DATE
